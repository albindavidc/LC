//258. Add Digits

var addDigits = function (num) {
    let sum = 0;
    num = num.toString().split('')

    for (i = 0; i < num.length; i++) {
        sum += parseInt(num[i])
    }

    if (sum > 9) {
        return addDigits(sum)
    }

    return sum
};
